  $(".accordion-header").each(function(){

     $(this).on("click",function(){ 

     if (this.children[0].getAttribute("class")=='fa fa-plus text-primary pull-right'){
  document.querySelectorAll("i[class='fa fa-minus text-primary pull-right']").forEach(em=>{
                                   em.setAttribute("class",'fa fa-plus text-primary pull-right')  
                                   })   
       this.children[0].setAttribute("class","fa fa-minus text-primary pull-right") 
        document.querySelectorAll(".active2").forEach(em=>{
                   $(em).slideUp(100)
                   console.log( em.parentElement);
                        em.parentElement.children[0].classList.remove("hc");
                                 }) 

          

           $(this.nextElementSibling).slideDown(100)
           $(this.nextElementSibling).addClass("active2")
           $(this).addClass("hc")
      }
        
      
     })

  })
</script>
